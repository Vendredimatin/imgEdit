	/****************************************
	* name:strAppearNumber
	* tips:字符串出现次数
	****************************************/
	function strAppearNumber(findStr,sourceStr){
		findStr=eval("/"+findStr+"/ig");
		if(sourceStr.match(findStr)==null||sourceStr.match(findStr)==undefined){
			return 0;
		}else{
			return sourceStr.match(findStr).length;
		}
	}
